function detectLang(): Lang {
  if (typeof window === "undefined") return "en";
  const nav = navigator.language.toLowerCase();
  for (const lang of SUPPORTED) {
    if (nav.startsWith(lang)) return lang;
  }
  // zh-CN, zh-TW → zh
  if (nav.startsWith("zh")) return "zh";
  return "en";
}

