function detectLang(): Lang {
  if (typeof window === "undefined") return "en";
  const nav = navigator.language.toLowerCase();
  for (const lang of SUPPORTED) {
    if (nav.startsWith(lang)) return lang;
  }
  // zh-CN, zh-TW → zh
  if (nav.startsWith("zh")) return "zh";
  return "en";
}

const currentLang: Lang = detectLang();

export function getLang(): Lang { return currentLang; }

export function t(key: string): string {
  return translations[currentLang]?.[key] || translations.en[key] || key;
}

export function getFreeFeatures(): string[] { return FREE_FEATURES[currentLang] || FREE_FEATURES.en; }
export function getProFeatures(): string[] { return PRO_FEATURES[currentLang] || PRO_FEATURES.en; }
