#!/usr/bin/env node
/**
 * Generate self-host secrets for docker-compose.
 *
 * Supabase self-host needs a JWT_SECRET plus two long-lived API keys (anon and
 * service_role) that are JWTs signed with that secret. This prints a ready-to-
 * paste block — no external tools, no web generator.
 *
 *   node scripts/gen-env-keys.mjs            # print to stdout
 *   node scripts/gen-env-keys.mjs >> .env    # append to your .env
 */
import { createHmac, randomBytes } from "node:crypto";

const b64url = (buf) =>
  Buffer.from(buf).toString("base64").replace(/\+/g, "-").replace(/\//g, "_").replace(/=+$/, "");

function signJwt(payload, secret) {
  const header = { alg: "HS256", typ: "JWT" };
  const data = `${b64url(JSON.stringify(header))}.${b64url(JSON.stringify(payload))}`;
  const sig = b64url(createHmac("sha256", secret).update(data).digest());
  return `${data}.${sig}`;
}

const JWT_SECRET = randomBytes(32).toString("hex"); // 64-char secret
const POSTGRES_PASSWORD = randomBytes(18).toString("base64").replace(/[^a-zA-Z0-9]/g, "").slice(0, 24);

const iat = 1700000000; // fixed past timestamp — keys are long-lived, value isn't security-sensitive
const exp = iat + 60 * 60 * 24 * 365 * 10; // ~10 years

const ANON_KEY = signJwt({ role: "anon", iss: "supabase", iat, exp }, JWT_SECRET);
const SERVICE_ROLE_KEY = signJwt({ role: "service_role", iss: "supabase", iat, exp }, JWT_SECRET);

process.stdout.write(
  [
    "# --- generated by scripts/gen-env-keys.mjs ---",
    `POSTGRES_PASSWORD=${POSTGRES_PASSWORD}`,
    `JWT_SECRET=${JWT_SECRET}`,
    `ANON_KEY=${ANON_KEY}`,
    `SERVICE_ROLE_KEY=${SERVICE_ROLE_KEY}`,
    "",
    "# App-facing copies (the app reads these names):",
    `NEXT_PUBLIC_SUPABASE_URL=http://localhost:8000`,
    `NEXT_PUBLIC_SUPABASE_ANON_KEY=${ANON_KEY}`,
    `SUPABASE_SERVICE_ROLE_KEY=${SERVICE_ROLE_KEY}`,
    "",
  ].join("\n")
);
